package pl.maniaq;

public class Human {
    String name;
    String lastName;
    int age;
    int height;
    float weight;

    public Human(String name, String lastName, int age, int height, float weight) {
        this.name = name;
        this.lastName = lastName;
        this.age = age;
        this.height = height;
        this.weight = weight;
    }
}
